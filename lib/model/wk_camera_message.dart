class WKCameraMessage {
  /// Camera close
  static const int CLOSE = 1;

  /// Camera open
  static const int OPEN = 2;

  /// Camera take photo(shutter)
  static const int TAKE_PHOTO = 3;

  /// Switch to camera back
  static const int CAMERA_BACK = 4;

  /// Switch to camera front
  static const int CAMERA_FRONT = 5;

  /// Switch flash off
  static const int FLASH_OFF = 6;

  /// Switch flash auto
  static const int FLASH_AUTO = 7;

  /// Switch flash on
  static const int FLASH_ON = 8;
}
